                            <div><strong>7.  Rights and Access to Information</strong></div>
                            <div style="padding-top: 10px;"><strong> USC honors the rights of data subjects with regards to access of stored personal information and to request its correction, update or deletion in applicable circumstances. You may request access to your personal information and in some instances, you will be able to access and update your personal information yourself through the Integrated School Management Information System portal. You may also access or correct your personal information by directly contacting the area specific unit of the University that holds your information such as the Office of the Registrar and the Information Resource Management Office among others.Where applicable you may be allowed the following (provided it is legal to do so):Rectify personal information held about you where it is incorrect or incomplete (right to rectification)</strong></div>
                            <div>
	                            <ul>
									<li>Request that your personal information be deleted if certain legal grounds are met (right to erasure)</li>
									<li>Rectify personal information held about you where it is incorrect or incomplete (right to rectification)</li>
									<li>Restrict or object to how your personal information is used (right to object and restrict processing); and</li>
									<li>Obtain a copy of your data in a commonly used electronic form, or request us in writing that we share your personal information with any person acting on your behalf (such as agents, guardians, employers, government departments) (right to portability).</li>                            	
	                            </ul>
