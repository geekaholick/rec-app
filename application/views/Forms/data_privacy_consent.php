                    <div class="row card-body">                        
                        <div class="col-lg-12" >                                   
                            <div><strong>4. How we use your information</strong></div>
                            <div style="padding-top: 10px;"><strong>USC uses personal and sensitive personal information as we perform and fulfill our core functions which includes teaching, research and community extension. We use your information according to the primary purpose for which it was collected and for other related purposes as required by law and with your consent. We will not share the information with third parties unless you give your written consent or there is an order from the court. We may use and process your information for:</strong></div>
                            <div>
	                            <ul>
									<li>Educational support: admission, enrolment, assessment/testing, learning activities, graduation, content delivery and learning activities, graduation, student services such as counselling, library use, medical examination and data analysis;</li>
									<li>Research: data analysis, commercialization and administration;</li>
									<li>Community extension and industry engagement: alumni relations, industry partnerships, website operation, event invitations and community forum;</li>
									<li>Employment: recruitment, payroll processing, employee development and other human resource programs and activities, annual physical examination;</li>
									<li>Operational/infrastructure management: processing fees, data analysis, financial management, IT, legal and professional services, and educational opportunities, CCTV monitoring, access to systems, identity management and emergency response;</li>
									<li>Non-academic matters in support of our core services, including: provision of student support services, managing student accommodation, parking within the campus, dealing with grievances and disciplinary actions;</li>
									<li>For other purposes permitted by law, such as the provision of information to government agencies and legal entities.</li>	                            	
	                            </ul>
                            </div>    
                        </div>                        
                    </div>


                    <div class="row card-body">                        
                        <div class="col-lg-12" >                                   
                            <div><strong>5. To whom we share your information</strong></div>
                            <div style="padding-top: 10px;"><strong>USC may share your personal data with certain third parties if called for by official business or transaction and in consonance with the provisions of the Data Privacy Act. We may disclose limited personal data to a variety of recipients including:</strong></div>
                            <div>
	                            <ul>
	                            	<li>Our employees and administrators;</li>
									<li>Third parties who have legitimate transactions with the University such as: agencies who work with us to provide health care and insurance, scholarship sponsors, current/potential education providers, funding bodies, current/potential employers for references, professional regulatory bodies with regards to the confirmation of qualifications, registration for professional examination/certification, government agencies where we have a legal obligation to provide information, third parties conducting researches and surveys. These recipients of the above-mentioned organizations and third party service providers are required to keep personal information confidential and provide the same privacy safeguards as we do in the University.</li>

	                            </ul>
                            </div>    
                        </div>                        
                    </div>



                    <div class="row card-body">                        
                        <div class="col-lg-12" >                                   
                            <div><strong>6. How we store and protect your information</strong></div>
                            <div style="padding-top: 10px;"><strong>USC takes appropriate security measures to protect your personal information from loss, and unauthorized access or disclosure.</strong></div>
                            <div>
	                            <ul>
									<li>Stored information is archived in accordance with the ICT Policy of the University which determines when information should be retained or disposed. Proper actions are taken into consideration to destroy and disposes personal information when it is no longer needed.</li>
									<li>Upon request, information will be destroyed unless applicable law requires destruction after the expiration of an applicable retention period. The manner of destruction shall be appropriate to preserve and ensure the confidentiality of your information given the level of sensitivity, value and criticality to the University.</li>
									<li>Personal information may be stored as hard/printed copies, as electronic data, in the University’s Integrated School Management Information System and other application systems in network or its designated repositories.</li>	                            	
	                            </ul>
                            </div>    
                        </div>                        
                    </div>




