                            <strong>General Privacy Statement</strong> 
                            <br><br>
                            <p>The University of San Carlos (USC) values and understands the importance of protecting the privacy of personal information and the confidentiality of data, information and knowledge and is committed to the responsible handling of such. This Privacy Policy Statement explains what information will be gathered and the details how collected information is used without breaching its privacy and confidentiality.</p>
                            <br>
                            <strong>Scope</strong> 
                            <br><br>
                            <p>   This Privacy Policy Statement applies to personal information about applicants, prospective applicants, students and employees maintained, used, processed and/or kept in custody by the University of San Carlos.</p>
                            <br>
                            <strong>Types of personal information:</strong> 
                            <br><br>
                            <ul>
                            	<li>Personal information: Recorded information about a living identifiable or easily identifiable individual</li>
							 	<li>Sensitive information: Personal information about a living individual's race or ethnicity, political opinions, religious or philosophical beliefs, sexual preferences or practices, criminal record, or membership details, such as trade union or professional, political or trade associations, genetic data and biometric data.</li>
								<li>Medical information: Information about a living or deceased individual's physical, mental or psychological health.</li>
							</ul>
                        </div>
                    </div>


                    <div class="row card-body">                        
                        <div class="col-lg-12" >                                   
                            <div><strong>1. Information that we collect</strong></div>
                            <div style="padding-top: 10px;"><strong>USC collects the following information:</strong></div>
                            <div>
                                
                            
                            <ul>
                                <li>Personal Information: name, residential address, email address, telephone number, date of birth, passport details (for international applicants) and nationality. USC will also assign you with a unique applicant/student identification number once you apply or is accepted in the University;</li>
                                <li>Education background employment history: the school(s) and/or universities you have attended, programs and course that have been completed, date(s) of completion, and the institution(s) where it was completed. For applicants for employment, we will also keep records of your work history, evaluation of your previous work, your past employers and other related service information;</li>
                                <li>Information about your family or personal affiliations, and academic and extracurricular interests since these maybe relevant to the assessment of your suitability to receive scholarships/student financial aids/assistance;</li>
                                <li>Sensitive personal information such as your political affiliations, sexual preferences or practices, criminal record, or memberships details, religious or philosophical beliefs and ethnicity among others;</li>
                                <li>Information concerning your health/medical conditions including your medical history, diagnosis of illness(es), disability and dietary needs.</li>
                            </ul>
                            </div>    
                        </div>                        
                    </div>


