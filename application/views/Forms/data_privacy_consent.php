                            <div><strong>2. How we collect your information</strong></div>
                            <div style="padding-top: 10px;"><strong>USC may collect your personal data in several ways:</strong></div>
                            <div>
                            <ul>
                            	<li>From the information you provide to the University when you contact us and you express your interest in studying at the University of San Carlos;</li>
                            	<li>When you apply to study at USC and complete application forms/enrollment forms and when you complete other admissions procedures;</li>                            
                            	<li>When you make inquiries, or communicate with us via email, through our University accounts in various social media platforms (Facebook, Instagram, Twitter, Youtube);</li>
								<li>Through other ways by which you interact us during your time as a student, employee, donor, or third party of USC, for example from your previous school, university or employers who may provide a reference about you.</li>

                            </ul>
                            </div>    
                        </div>                        
